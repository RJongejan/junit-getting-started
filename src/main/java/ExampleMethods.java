public class ExampleMethods {

    public static int plusFive(int i){
        return i + 5;
    }

    public static String stringToStling(String string){
        return string.replaceAll("r", "l");
    }

    public static String toUppercase(String input){
        return input.toUpperCase();
    }

    public static String stitchTogether(String first, String second){
        return first + second;
    }
}
