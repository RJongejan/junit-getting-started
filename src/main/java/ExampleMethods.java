public class ExampleMethods {

    public int plusFive(int i){
        return i + 5;
    }

    public String stringToStling(String string){
        return string.replaceAll("r", "l");
    }

    public String toUppercase(String input){
        return input.toUpperCase();
    }

    public String stitchTogether(String first, String second){
        return first + second;
    }
}
